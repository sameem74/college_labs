public class Floatpoint {
    public static void main (String args[]){
        float a= 21.5F, b=4.7F;
        System.out.println(" a = "  +a);
        System.out.println(" b="  +b);
        System.out.println(" a+b= "  +(a+b));
        System.out.println(" a-b="  +(a-b));
        System.out.println(" a*b=" +(a*b));
        System.out.println(" a/b="  +(a/b));
        System.out.println(" a%b="  +(a%b));
        }
        
}
